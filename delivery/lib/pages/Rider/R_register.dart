  final TextEditingController vehicleController = TextEditingController();
  final ImagePicker picker = ImagePicker();
  XFile? riderProfileImage;
  XFile? vehicleImage;
  bool isLoading = false; // ‚úÖ ‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞‡πÇ‡∏´‡∏•‡∏î‡∏î‡∏¥‡πâ‡∏á

  @override
