      body: Center(
        child: SingleChildScrollView(
          child: Container(
            padding: const EdgeInsets.all(20),
            margin: const EdgeInsets.symmetric(horizontal: 24, vertical: 40),
            decoration: BoxDecoration(
              color: Colors.white,
              borderRadius: BorderRadius.circular(16),
              boxShadow: [
                BoxShadow(
                  color: Colors.black.withOpacity(0.1),
                  blurRadius: 8,
                  offset: const Offset(0, 4),
                ),
              ],
            ),
            child: Column(
              children: [
                _buildTextField(usernameController, "‡∏ä‡∏∑‡πà‡∏≠‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ‡∏á‡∏≤‡∏ô"),
                const SizedBox(height: 16),
                _buildTextField(
                  phoneController,
                  "‡∏´‡∏°‡∏≤‡∏¢‡πÄ‡∏•‡∏Ç‡πÇ‡∏ó‡∏£‡∏®‡∏±‡∏û‡∏ó‡πå",
                  keyboardType: TextInputType.phone,
                ),
                const SizedBox(height: 16),
                _buildTextField(passwordController, "‡∏£‡∏´‡∏±‡∏™‡∏ú‡πà‡∏≤‡∏ô", obscure: true),
                const SizedBox(height: 16),
                _buildTextField(
                  confirmPasswordController,
                  "‡∏¢‡∏∑‡∏ô‡∏¢‡∏±‡∏ô‡∏£‡∏´‡∏±‡∏™‡∏ú‡πà‡∏≤‡∏ô",
                  obscure: true,
                ),
                const SizedBox(height: 16),
                _buildTextField(vehicleController, "‡∏ó‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ô‡∏£‡∏ñ"),
                const SizedBox(height: 16),

                // ‚úÖ ‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡∏£‡∏π‡∏õ‡πÇ‡∏õ‡∏£‡πÑ‡∏ü‡∏•‡πå
                _buildUploadBox(
                  title: "‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏£‡∏π‡∏õ‡πÇ‡∏õ‡∏£‡πÑ‡∏ü‡∏•‡πå‡πÑ‡∏£‡πÄ‡∏î‡∏≠‡∏£‡πå",
                  pickedFile: riderProfileImage,
                  onPick: () async {
                    final picked = await picker.pickImage(
                      source: ImageSource.gallery,
                    );
                    if (picked != null)
                      setState(() => riderProfileImage = picked);
                  },
                ),
                const SizedBox(height: 16),

                // ‚úÖ ‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡∏£‡∏π‡∏õ‡∏£‡∏ñ
                _buildUploadBox(
                  title: "‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏£‡∏π‡∏õ‡∏¢‡∏≤‡∏ô‡∏û‡∏≤‡∏´‡∏ô‡∏∞",
                  pickedFile: vehicleImage,
                  onPick: () async {
                    final picked = await picker.pickImage(
                      source: ImageSource.gallery,
                    );
                    if (picked != null) setState(() => vehicleImage = picked);
                  },
                ),
                const SizedBox(height: 24),

                // ‚úÖ ‡∏õ‡∏∏‡πà‡∏°‡∏™‡∏°‡∏±‡∏Ñ‡∏£
                SizedBox(
                  width: double.infinity,
                  child: ElevatedButton(
                    style: ElevatedButton.styleFrom(
                      backgroundColor: isLoading ? Colors.grey : Colors.black87,
                      padding: const EdgeInsets.symmetric(vertical: 14),
                      shape: RoundedRectangleBorder(
                        borderRadius: BorderRadius.circular(8),
                      ),
                    ),
                    onPressed: isLoading ? null : addDataRider,
                    child: isLoading
                        ? const Row(
                            mainAxisAlignment: MainAxisAlignment.center,
                            children: [
                              SizedBox(
                                width: 22,
                                height: 22,
                                child: CircularProgressIndicator(
                                  color: Colors.white,
                                  strokeWidth: 2.5,
                                ),
                              ),
                              SizedBox(width: 12),
                              Text(
                                "‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏•‡∏á‡∏ó‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ô...",
                                style: TextStyle(color: Colors.white),
                              ),
                            ],
                          )
                        : const Text(
                            "‡∏•‡∏á‡∏ó‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ô Rider",
                            style: TextStyle(fontSize: 16, color: Colors.white),
                          ),
                  ),
                ),
                const SizedBox(height: 12),

                TextButton(
                  onPressed: () => Get.to(() => const ULoginPage()),
                  child: const Text(
                    "Sign in",
                    style: TextStyle(
                      fontSize: 16,
                      color: Colors.blue,
                      fontWeight: FontWeight.bold,
                    ),
                  ),
                ),
              ],
            ),
          ),
        ),
      ),
    );
  }

  /// üîπ ‡∏ä‡πà‡∏≠‡∏á‡∏Å‡∏£‡∏≠‡∏Å‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏û‡∏∑‡πâ‡∏ô‡∏ê‡∏≤‡∏ô
  Widget _buildTextField(
    TextEditingController controller,
    String label, {
    bool obscure = false,
    TextInputType? keyboardType,
  }) {
    return TextField(
      controller: controller,
      obscureText: obscure,
      keyboardType: keyboardType,
      decoration: InputDecoration(
        labelText: label,
        border: const OutlineInputBorder(),
      ),
    );
  }

  /// üîπ ‡∏Å‡∏•‡πà‡∏≠‡∏á‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡∏£‡∏π‡∏õ
  Widget _buildUploadBox({
    required String title,
    required XFile? pickedFile,
    required Function() onPick,
  }) {
    return InkWell(
      onTap: onPick,
      child: Container(
        width: double.infinity,
        padding: const EdgeInsets.symmetric(horizontal: 12, vertical: 16),
        decoration: BoxDecoration(
          border: Border.all(color: Colors.grey.shade400),
          borderRadius: BorderRadius.circular(8),
        ),
        child: Row(
          mainAxisAlignment: MainAxisAlignment.spaceBetween,
          children: [
            Expanded(
              child: Text(
                pickedFile != null ? pickedFile.name : title,
                overflow: TextOverflow.ellipsis,
                style: const TextStyle(fontSize: 16, color: Colors.black87),
              ),
            ),
            const Icon(Icons.file_upload_outlined, color: Colors.black54),
          ],
        ),
      ),
    );
  }

  /// ‚úÖ ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏•‡∏á‡∏ó‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ô Rider (‡∏°‡∏µ‡πÇ‡∏´‡∏•‡∏î‡∏î‡∏¥‡πâ‡∏á + ‡∏ï‡∏£‡∏ß‡∏à‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏Ñ‡∏£‡∏ö)
  void addDataRider() async {
    final username = usernameController.text.trim();
    final phone = phoneController.text.trim();
    final password = passwordController.text.trim();
    final confirm = confirmPasswordController.text.trim();
    final vehicle = vehicleController.text.trim();

    // üî∏ ‡∏ï‡∏£‡∏ß‡∏à‡∏ä‡πà‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î
    if (username.isEmpty ||
        phone.isEmpty ||
        password.isEmpty ||
        confirm.isEmpty ||
        vehicle.isEmpty ||
        riderProfileImage == null ||
        vehicleImage == null) {
      Get.snackbar(
        "‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÑ‡∏°‡πà‡∏Ñ‡∏£‡∏ö",
        "‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏Å‡∏£‡∏≠‡∏Å‡∏ó‡∏∏‡∏Å‡∏ä‡πà‡∏≠‡∏á‡πÅ‡∏•‡∏∞‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏£‡∏π‡∏õ‡∏†‡∏≤‡∏û‡πÉ‡∏´‡πâ‡∏Ñ‡∏£‡∏ö",
        backgroundColor: Colors.orange,
        colorText: Colors.white,
      );
      return;
    }

    // üî∏ ‡∏ï‡∏£‡∏ß‡∏à‡∏û‡∏≤‡∏™‡πÑ‡∏°‡πà‡∏ï‡∏£‡∏á‡∏Å‡∏±‡∏ô
    if (password != confirm) {
      Get.snackbar(
        "‡∏£‡∏´‡∏±‡∏™‡∏ú‡πà‡∏≤‡∏ô‡πÑ‡∏°‡πà‡∏ï‡∏£‡∏á‡∏Å‡∏±‡∏ô",
        "‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏Å‡∏£‡∏≠‡∏Å‡πÉ‡∏´‡πâ‡∏ï‡∏£‡∏á‡∏Å‡∏±‡∏ô",
        backgroundColor: Colors.red,
        colorText: Colors.white,
      );
      return;
    }

    // ‚úÖ ‡πÄ‡∏£‡∏¥‡πà‡∏°‡πÇ‡∏´‡∏•‡∏î‡∏î‡∏¥‡πâ‡∏á
    setState(() => isLoading = true);
    Get.dialog(
      const Center(child: CircularProgressIndicator()),
      barrierDismissible: false,
    );

    try {
      final supabase = Supabase.instance.client;
      final hashedPassword = sha256.convert(utf8.encode(password)).toString();

      String? riderImageUrl;
      String? vehicleImageUrl;
      final userDoc = await FirebaseFirestore.instance
          .collection('user')
          .doc(username)
          .get();
      final riderDoc = await FirebaseFirestore.instance
          .collection('rider')
          .doc(username)
          .get();

      if (userDoc.exists || riderDoc.exists) {
        if (Get.isDialogOpen ?? false) Get.back();
        Get.snackbar(
          '‡∏ä‡∏∑‡πà‡∏≠‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ‡∏á‡∏≤‡∏ô‡∏ã‡πâ‡∏≥',
          '‡∏ä‡∏∑‡πà‡∏≠ "$username" ‡∏ñ‡∏π‡∏Å‡πÉ‡∏ä‡πâ‡πÅ‡∏•‡πâ‡∏ß ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏ä‡∏∑‡πà‡∏≠‡∏≠‡∏∑‡πà‡∏ô',
          backgroundColor: Colors.red,
          colorText: Colors.white,
        );
        setState(() => isLoading = false);
        return; // ‚ùå ‡∏´‡∏¢‡∏∏‡∏î‡∏Å‡∏≤‡∏£‡∏ó‡∏≥‡∏á‡∏≤‡∏ô
      }

      // üîπ ‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡∏£‡∏π‡∏õ‡πÇ‡∏õ‡∏£‡πÑ‡∏ü‡∏•‡πå
      if (riderProfileImage != null) {
        final file = File(riderProfileImage!.path);
        final fileName =
            "rider_profiles/${DateTime.now().millisecondsSinceEpoch}_${riderProfileImage!.name}";
        await supabase.storage.from('rider').upload(fileName, file);
        riderImageUrl = supabase.storage.from('rider').getPublicUrl(fileName);
      }

      // üîπ ‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡∏£‡∏π‡∏õ‡∏£‡∏ñ
      if (vehicleImage != null) {
        final file = File(vehicleImage!.path);
        final fileName =
            "vehicle_images/${DateTime.now().millisecondsSinceEpoch}_${vehicleImage!.name}";
        await supabase.storage.from('rider').upload(fileName, file);
        vehicleImageUrl = supabase.storage.from('rider').getPublicUrl(fileName);
      }

      // üîπ ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å Firestore
      final data = {
        "username": username,
        "phone": phone,
        "password": hashedPassword,
        "vehicleController": vehicle,
        "riderImageUrl": riderImageUrl ?? "",
        "vehicleImageUrl": vehicleImageUrl ?? "",
      };

      await FirebaseFirestore.instance
          .collection('rider')
          .doc(username)
          .set(data);

      if (Get.isDialogOpen ?? false) Get.back();
      Get.snackbar(
        "‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à",
        "‡∏•‡∏á‡∏ó‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ô‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à‡πÅ‡∏•‡πâ‡∏ß üéâ",
        backgroundColor: Colors.green,
        colorText: Colors.white,
      );
      Get.offAll(() => const ULoginPage());
    } catch (e, stack) {
      if (Get.isDialogOpen ?? false) Get.back();
      log("‚ùå Error: $e\n$stack");
      Get.snackbar(
        "‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î",
        "‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÑ‡∏°‡πà‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à: $e",
        backgroundColor: Colors.red,
        colorText: Colors.white,
      );
    } finally {
      setState(() => isLoading = false);
    }
  }
}
